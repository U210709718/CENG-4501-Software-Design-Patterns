public class RedHeadDuck extends Duck {

    @Override
    public void quack() {
        System.out.println("Quack Quack");
    }

    @Override
    public void fly() {
        System.out.println("I'm flying");
    }

    @Override
    public void swim() {
        System.out.println("I'm swimming");
    }

    @Override
    public void display() {
        System.out.println("I'm a redHead duck");
    }
    
}
